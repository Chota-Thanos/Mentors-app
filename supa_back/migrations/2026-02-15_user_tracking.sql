  for insert with check (auth.uid() = user_id);

-- User AI Mains Questions (Generated History)
create table if not exists user_ai_mains_questions (
  id bigint generated by default as identity primary key,
  question_text text not null,
  answer_approach text,
  model_answer text,
  word_limit int default 150,
  source_reference text,
  author_id uuid references auth.users(id),
  created_at timestamptz default now(),
  expires_at timestamptz
);

alter table user_ai_mains_questions enable row level security;

create policy "Users can view their own generated questions" on user_ai_mains_questions
  for select using (auth.uid() = author_id);

create policy "Users can insert their own generated questions" on user_ai_mains_questions
  for insert with check (auth.uid() = author_id);
