-- =============================================================
-- MIGRATION 11: Results & Analytics
-- Per-question answer tracking, performance snapshots, weak areas
-- =============================================================

-- ---------------------------------------------------------------
-- TEST ATTEMPTS
-- ---------------------------------------------------------------

CREATE TABLE IF NOT EXISTS public.test_attempts (
  id                    BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  user_id               BIGINT NOT NULL REFERENCES public.profiles(id) ON DELETE CASCADE,
  premium_collection_id BIGINT REFERENCES public.premium_collections(id) ON DELETE CASCADE,
  test_series_id        BIGINT REFERENCES public.test_series(id) ON DELETE SET NULL,
  attempt_timestamp     TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

CREATE INDEX IF NOT EXISTS idx_test_attempts_user       ON public.test_attempts(user_id);
CREATE INDEX IF NOT EXISTS idx_test_attempts_collection ON public.test_attempts(premium_collection_id);
CREATE INDEX IF NOT EXISTS idx_test_attempts_series     ON public.test_attempts(test_series_id);
CREATE INDEX IF NOT EXISTS idx_test_attempts_timestamp  ON public.test_attempts(attempt_timestamp DESC);

-- ---------------------------------------------------------------
-- TEST RESULTS (Aggregate per attempt)
-- ---------------------------------------------------------------

CREATE TABLE IF NOT EXISTS public.test_results (
  id               BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  attempt_id       BIGINT NOT NULL UNIQUE REFERENCES public.test_attempts(id) ON DELETE CASCADE,
  score            NUMERIC(10,2) NOT NULL DEFAULT 0,
  total_questions  INTEGER NOT NULL DEFAULT 0,
  correct_answers  INTEGER NOT NULL DEFAULT 0,
  incorrect_answers INTEGER NOT NULL DEFAULT 0,
  unanswered       INTEGER NOT NULL DEFAULT 0,
  time_taken_secs  INTEGER,
  completed_at     TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

CREATE INDEX IF NOT EXISTS idx_test_results_attempt ON public.test_results(attempt_id);

-- ---------------------------------------------------------------
-- QUIZ ATTEMPT ANSWERS (PER-QUESTION tracking — no JSONB blobs)
-- This enables category-wise and question-wise analytics
-- ---------------------------------------------------------------

CREATE TABLE IF NOT EXISTS public.quiz_attempt_answers (
  id               BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  attempt_id       BIGINT NOT NULL REFERENCES public.test_attempts(id) ON DELETE CASCADE,
  -- Which question was answered
  quiz_id          BIGINT REFERENCES public.quizzes(id) ON DELETE SET NULL,
  passage_quiz_id  BIGINT REFERENCES public.passage_quizzes(id) ON DELETE SET NULL,
  passage_question_id BIGINT REFERENCES public.passage_questions(id) ON DELETE SET NULL,
  -- Position in the test
  question_index   INTEGER NOT NULL,
  -- Answer data
  selected_option  TEXT,
  correct_answer   TEXT NOT NULL,
  is_correct       BOOLEAN NOT NULL,
  is_skipped       BOOLEAN NOT NULL DEFAULT FALSE,
  time_spent_secs  INTEGER,
  -- Category chain — as deep as assigned
  category_id      BIGINT REFERENCES public.categories(id) ON DELETE SET NULL,
  created_at       TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

CREATE INDEX IF NOT EXISTS idx_qaa_attempt    ON public.quiz_attempt_answers(attempt_id);
CREATE INDEX IF NOT EXISTS idx_qaa_quiz       ON public.quiz_attempt_answers(quiz_id);
CREATE INDEX IF NOT EXISTS idx_qaa_category   ON public.quiz_attempt_answers(category_id);
CREATE INDEX IF NOT EXISTS idx_qaa_is_correct ON public.quiz_attempt_answers(is_correct);

-- ---------------------------------------------------------------
-- USER PERFORMANCE SNAPSHOTS
-- Computed/cached accuracy per user per domain per category
-- Rebuilt after each attempt (or via background job)
-- ---------------------------------------------------------------

CREATE TABLE IF NOT EXISTS public.user_performance_snapshots (
  id              BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  user_id         BIGINT NOT NULL REFERENCES public.profiles(id) ON DELETE CASCADE,
  quiz_domain     TEXT   NOT NULL CHECK (quiz_domain IN ('gk', 'maths', 'passage', 'mains')),
  category_id     BIGINT REFERENCES public.categories(id) ON DELETE CASCADE,
  -- Aggregated stats
  total_attempts  INTEGER     NOT NULL DEFAULT 0,
  total_questions INTEGER     NOT NULL DEFAULT 0,
  correct_count   INTEGER     NOT NULL DEFAULT 0,
  incorrect_count INTEGER     NOT NULL DEFAULT 0,
  skipped_count   INTEGER     NOT NULL DEFAULT 0,
  accuracy        NUMERIC(5,2),            -- correct / attempted * 100
  avg_time_secs   NUMERIC(8,2),
  last_attempted  TIMESTAMPTZ,
  updated_at      TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  UNIQUE (user_id, quiz_domain, category_id)
);

CREATE INDEX IF NOT EXISTS idx_perf_snap_user      ON public.user_performance_snapshots(user_id);
CREATE INDEX IF NOT EXISTS idx_perf_snap_category  ON public.user_performance_snapshots(category_id);
CREATE INDEX IF NOT EXISTS idx_perf_snap_accuracy  ON public.user_performance_snapshots(user_id, quiz_domain, accuracy);

-- ---------------------------------------------------------------
-- USER WEAK AREAS (AI-determined, stored persistently)
-- ---------------------------------------------------------------

CREATE TABLE IF NOT EXISTS public.user_weak_areas (
  id            BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  user_id       BIGINT NOT NULL REFERENCES public.profiles(id) ON DELETE CASCADE,
  category_id   BIGINT NOT NULL REFERENCES public.categories(id) ON DELETE CASCADE,
  quiz_domain   TEXT   NOT NULL,
  severity      TEXT   NOT NULL DEFAULT 'moderate'
    CHECK (severity IN ('critical', 'moderate', 'mild')),
  accuracy      NUMERIC(5,2),
  generated_at  TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  UNIQUE (user_id, category_id, quiz_domain)
);

CREATE INDEX IF NOT EXISTS idx_weak_areas_user     ON public.user_weak_areas(user_id);
CREATE INDEX IF NOT EXISTS idx_weak_areas_category ON public.user_weak_areas(category_id);

-- ---------------------------------------------------------------
-- QUIZ QUESTION COMPLAINTS
-- ---------------------------------------------------------------

CREATE TABLE IF NOT EXISTS public.reported_questions (
  id              BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  reporter_id     BIGINT REFERENCES public.profiles(id) ON DELETE SET NULL,
  question_type   TEXT   NOT NULL CHECK (question_type IN ('gk_quiz', 'maths_quiz', 'passage_quiz', 'passage_question', 'ai_test_question')),
  question_id     BIGINT,
  collection_id   BIGINT REFERENCES public.premium_collections(id) ON DELETE SET NULL,
  attempt_id      BIGINT REFERENCES public.test_attempts(id) ON DELETE SET NULL,
  question_number INTEGER,
  question_text   TEXT,
  selected_option TEXT,
  correct_answer  TEXT,
  reason          TEXT   NOT NULL,
  details         JSONB  NOT NULL DEFAULT '{}'::JSONB,
  status          TEXT   NOT NULL DEFAULT 'open'
    CHECK (status IN ('open', 'reviewing', 'resolved', 'dismissed')),
  admin_notes     TEXT,
  resolved_at     TIMESTAMPTZ,
  created_at      TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  updated_at      TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

CREATE INDEX IF NOT EXISTS idx_reported_questions_status   ON public.reported_questions(status);
CREATE INDEX IF NOT EXISTS idx_reported_questions_reporter ON public.reported_questions(reporter_id);

DROP TRIGGER IF EXISTS trg_reported_questions_set_updated_at ON public.reported_questions;
CREATE TRIGGER trg_reported_questions_set_updated_at
  BEFORE UPDATE ON public.reported_questions
  FOR EACH ROW EXECUTE FUNCTION public.set_updated_at();

