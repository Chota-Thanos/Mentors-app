-- Test attempts: own
DROP POLICY IF EXISTS "test_attempts_own" ON public.test_attempts;
CREATE POLICY "test_attempts_own" ON public.test_attempts FOR ALL TO authenticated
  USING (
    public.is_admin_or_moderator()
    OR EXISTS (SELECT 1 FROM public.profiles p WHERE p.id = test_attempts.user_id AND p.auth_user_id = auth.uid())
  )
  WITH CHECK (EXISTS (SELECT 1 FROM public.profiles p WHERE p.id = test_attempts.user_id AND p.auth_user_id = auth.uid()));

-- Test results: own
DROP POLICY IF EXISTS "test_results_own" ON public.test_results;
CREATE POLICY "test_results_own" ON public.test_results FOR ALL TO authenticated
  USING (
    public.is_admin_or_moderator()
    OR EXISTS (
      SELECT 1 FROM public.test_attempts ta JOIN public.profiles p ON p.id = ta.user_id
      WHERE ta.id = test_results.attempt_id AND p.auth_user_id = auth.uid()
    )
  )
  WITH CHECK (
    EXISTS (
      SELECT 1 FROM public.test_attempts ta JOIN public.profiles p ON p.id = ta.user_id
      WHERE ta.id = test_results.attempt_id AND p.auth_user_id = auth.uid()
    )
  );

