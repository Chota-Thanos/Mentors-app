ALTER TABLE public.creator_applications ENABLE ROW LEVEL SECURITY;
ALTER TABLE public.admin_subpages       ENABLE ROW LEVEL SECURITY;
ALTER TABLE public.user_activities      ENABLE ROW LEVEL SECURITY;
ALTER TABLE public.study_materials      ENABLE ROW LEVEL SECURITY;

-- Creator applications: own user read/insert, admin all
DROP POLICY IF EXISTS "creator_apps_own_read" ON public.creator_applications;
CREATE POLICY "creator_apps_own_read" ON public.creator_applications FOR SELECT TO authenticated
  USING (
    public.is_admin_or_moderator()
    OR EXISTS (SELECT 1 FROM public.profiles p WHERE p.id = creator_applications.user_id AND p.auth_user_id = auth.uid())
  );

DROP POLICY IF EXISTS "creator_apps_own_insert" ON public.creator_applications;
CREATE POLICY "creator_apps_own_insert" ON public.creator_applications FOR INSERT TO authenticated
  WITH CHECK (EXISTS (SELECT 1 FROM public.profiles p WHERE p.id = creator_applications.user_id AND p.auth_user_id = auth.uid()));

DROP POLICY IF EXISTS "creator_apps_admin_update" ON public.creator_applications;
CREATE POLICY "creator_apps_admin_update" ON public.creator_applications FOR UPDATE TO authenticated
  USING (public.is_admin_or_moderator()) WITH CHECK (public.is_admin_or_moderator());

-- Admin subpages: public read, admin write
DROP POLICY IF EXISTS "admin_subpages_public_read" ON public.admin_subpages;
CREATE POLICY "admin_subpages_public_read" ON public.admin_subpages FOR SELECT TO anon, authenticated USING (TRUE);
DROP POLICY IF EXISTS "admin_subpages_admin_write" ON public.admin_subpages;
CREATE POLICY "admin_subpages_admin_write" ON public.admin_subpages FOR ALL TO authenticated
  USING (public.is_admin_or_moderator()) WITH CHECK (public.is_admin_or_moderator());

-- User activities: admin read, system insert
DROP POLICY IF EXISTS "user_activities_admin_read" ON public.user_activities;
CREATE POLICY "user_activities_admin_read" ON public.user_activities FOR SELECT TO authenticated
  USING (public.is_admin_or_moderator());

-- Study materials: public read, admin write
DROP POLICY IF EXISTS "study_materials_public_read" ON public.study_materials;
CREATE POLICY "study_materials_public_read" ON public.study_materials FOR SELECT TO anon, authenticated USING (TRUE);
DROP POLICY IF EXISTS "study_materials_admin_write" ON public.study_materials;
CREATE POLICY "study_materials_admin_write" ON public.study_materials FOR ALL TO authenticated
  USING (public.is_admin_or_moderator()) WITH CHECK (public.is_admin_or_moderator());

