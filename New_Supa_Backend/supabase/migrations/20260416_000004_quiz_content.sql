-- Public read for all quiz content
DO $$
DECLARE
  tbls TEXT[] := ARRAY[
    'quizzes','quiz_categories','quiz_free_exams','quiz_alpha_categories',
    'quiz_likes','quiz_comments','passage_quizzes','passage_questions',
    'passage_quiz_categories','passage_quiz_free_exams','passage_quiz_alpha_categories',
    'passage_quiz_likes','passage_quiz_comments',
    'mains_questions','knowledge_tests'
  ];
  t TEXT;
BEGIN
  FOREACH t IN ARRAY tbls LOOP
    EXECUTE FORMAT('DROP POLICY IF EXISTS "%s_public_read" ON public.%I', t, t);
    EXECUTE FORMAT(
      'CREATE POLICY "%s_public_read" ON public.%I FOR SELECT TO anon, authenticated USING (TRUE)', t, t
    );
    EXECUTE FORMAT('DROP POLICY IF EXISTS "%s_admin_write" ON public.%I', t, t);
    EXECUTE FORMAT(
      'CREATE POLICY "%s_admin_write" ON public.%I FOR ALL TO authenticated
       USING (public.is_admin_or_moderator()) WITH CHECK (public.is_admin_or_moderator())',
      t, t
    );
  END LOOP;
END;
$$;

-- Quiz likes — owned by users
DROP POLICY IF EXISTS "quiz_likes_insert_own" ON public.quiz_likes;
CREATE POLICY "quiz_likes_insert_own" ON public.quiz_likes
  FOR INSERT TO authenticated
  WITH CHECK (EXISTS (SELECT 1 FROM public.profiles p WHERE p.id = quiz_likes.user_id AND p.auth_user_id = auth.uid()));

DROP POLICY IF EXISTS "quiz_likes_delete_own" ON public.quiz_likes;
