-- *** KEY DECISION: passage sub-questions as JOIN TABLE ***
-- Each question belongs to one passage, enforced via CASCADE DELETE
-- Enables per-question analytics without losing tight coupling
CREATE TABLE IF NOT EXISTS public.passage_questions (
  id                      BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  passage_quiz_id         BIGINT NOT NULL REFERENCES public.passage_quizzes(id) ON DELETE CASCADE,
  question_statement      TEXT   NOT NULL,
  supp_question_statement TEXT,
  question_prompt         TEXT,
  statements_facts        JSONB  NOT NULL DEFAULT '[]'::JSONB,
  options                 JSONB  NOT NULL DEFAULT '[]'::JSONB,
  -- [{ "label": "A", "text": "..." }, ...]
  correct_answer          TEXT   NOT NULL,
  explanation             TEXT,
  -- Category for analytics depth
  category_id             BIGINT REFERENCES public.categories(id) ON DELETE SET NULL,
  display_order           INTEGER NOT NULL DEFAULT 0,
  created_at              TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  updated_at              TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

CREATE INDEX IF NOT EXISTS idx_passage_questions_passage  ON public.passage_questions(passage_quiz_id);
CREATE INDEX IF NOT EXISTS idx_passage_questions_category ON public.passage_questions(category_id);

DROP TRIGGER IF EXISTS trg_passage_questions_set_updated_at ON public.passage_questions;
CREATE TRIGGER trg_passage_questions_set_updated_at
  BEFORE UPDATE ON public.passage_questions
  FOR EACH ROW EXECUTE FUNCTION public.set_updated_at();

