-- ---------------------------------------------------------------
-- GK / MATHS QUIZZES
-- Full question structure with all UPSC-specific fields
-- ---------------------------------------------------------------

CREATE TABLE IF NOT EXISTS public.quizzes (
  id                       BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  quiz_type                TEXT NOT NULL CHECK (quiz_type IN ('gk', 'maths')),
  title                    TEXT,
  slug                     TEXT UNIQUE,

  -- Question content
  question_statement       TEXT NOT NULL,
  supp_question_statement  TEXT,
  statements_facts         JSONB NOT NULL DEFAULT '[]'::JSONB,
  -- e.g. [{ "number": 1, "text": "India shares border with 7 nations" }, ...]
  question_prompt          TEXT,
  -- e.g. "Which of the following statements is/are correct?"
  options                  JSONB NOT NULL DEFAULT '[]'::JSONB,
  -- e.g. [{ "label": "A", "text": "1 only" }, { "label": "B", "text": "1 and 2 only" }]
  correct_answer           TEXT NOT NULL,
  explanation              TEXT,
  linked_explanation       TEXT,   -- URL to article / study page

  -- Contextual enrichment
  why_this_quiz            JSONB NOT NULL DEFAULT '[]'::JSONB,
  -- e.g. ["Current Affairs", "Environment"]
  why_this_quiz_text       TEXT,
  keywords                 JSONB NOT NULL DEFAULT '[]'::JSONB,
  sources                  JSONB NOT NULL DEFAULT '[]'::JSONB,
  -- e.g. [{ "title": "The Hindu", "url": "...", "date": "2026-04-10" }]

  -- Image support (with hotspot overlay for diagram questions)
  image_url                TEXT,
  image_hotspot_overlay    JSONB,

  -- Test settings
  difficulty               TEXT CHECK (difficulty IN ('easy', 'medium', 'hard')),
  time_limit_minutes       INTEGER,
  negative_marking_factor  NUMERIC(4,2) NOT NULL DEFAULT 0.33,

  -- Visibility
  is_pinned                BOOLEAN NOT NULL DEFAULT FALSE,
  visible_on_pages         TEXT[]  NOT NULL DEFAULT '{}',
  is_admin_only            BOOLEAN NOT NULL DEFAULT FALSE,

  -- Authorship & timestamps
  author_id                BIGINT NOT NULL REFERENCES public.profiles(id) ON DELETE RESTRICT,
  posted_at                TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  created_at               TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  updated_at               TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

CREATE INDEX IF NOT EXISTS idx_quizzes_quiz_type  ON public.quizzes(quiz_type);
CREATE INDEX IF NOT EXISTS idx_quizzes_posted_at  ON public.quizzes(posted_at DESC);
CREATE INDEX IF NOT EXISTS idx_quizzes_author_id  ON public.quizzes(author_id);
CREATE INDEX IF NOT EXISTS idx_quizzes_difficulty ON public.quizzes(difficulty);

DROP TRIGGER IF EXISTS trg_quizzes_set_updated_at ON public.quizzes;
CREATE TRIGGER trg_quizzes_set_updated_at
  BEFORE UPDATE ON public.quizzes
  FOR EACH ROW EXECUTE FUNCTION public.set_updated_at();

-- Quiz → Unified Category
