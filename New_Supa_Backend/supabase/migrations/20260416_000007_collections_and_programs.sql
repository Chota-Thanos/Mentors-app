-- ---------------------------------------------------------------
-- PREMIUM COLLECTIONS (Tests / Mains Sets created by experts)
-- ---------------------------------------------------------------

CREATE TABLE IF NOT EXISTS public.premium_collections (
  id                    BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  name                  TEXT NOT NULL,
  slug                  TEXT UNIQUE,
  description           TEXT,
  collection_type       TEXT NOT NULL DEFAULT 'prelims'
    CHECK (collection_type IN ('prelims', 'mains', 'mixed')),
  image_url             TEXT,

  -- Access control
  is_paid               BOOLEAN NOT NULL DEFAULT FALSE,
  is_public             BOOLEAN NOT NULL DEFAULT FALSE,
  is_subscription       BOOLEAN NOT NULL DEFAULT FALSE,
  price                 NUMERIC(10,2),

  -- Test settings (for prelims)
  time_limit_minutes    INTEGER,
  negative_marking_factor NUMERIC(4,2) NOT NULL DEFAULT 0.33,
  shuffle_questions     BOOLEAN NOT NULL DEFAULT FALSE,

  -- State
  is_finalized          BOOLEAN NOT NULL DEFAULT FALSE,
  is_active             BOOLEAN NOT NULL DEFAULT TRUE,

  -- Authorship
  creator_id            BIGINT NOT NULL REFERENCES public.profiles(id) ON DELETE RESTRICT,

  -- Source tracking (how this collection was built)
  source_category_ids   BIGINT[]    NOT NULL DEFAULT '{}',
  source_pdf_url        TEXT,
  source_content_html   TEXT,

  created_at            TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  updated_at            TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

CREATE INDEX IF NOT EXISTS idx_prem_collections_creator  ON public.premium_collections(creator_id);
CREATE INDEX IF NOT EXISTS idx_prem_collections_flags    ON public.premium_collections(is_public, is_paid, collection_type);

DROP TRIGGER IF EXISTS trg_premium_collections_set_updated_at ON public.premium_collections;
CREATE TRIGGER trg_premium_collections_set_updated_at
  BEFORE UPDATE ON public.premium_collections
  FOR EACH ROW EXECUTE FUNCTION public.set_updated_at();

-- Collection → Alpha Category
CREATE TABLE IF NOT EXISTS public.premium_collection_alpha_categories (
  premium_collection_id BIGINT NOT NULL REFERENCES public.premium_collections(id) ON DELETE CASCADE,
  alpha_category_id     BIGINT NOT NULL REFERENCES public.alpha_categories(id) ON DELETE CASCADE,
  PRIMARY KEY (premium_collection_id, alpha_category_id)
);

-- ---------------------------------------------------------------
-- PREMIUM COLLECTION ITEMS
-- Each item links to an actual quiz/question row — no JSONB snapshots
-- ---------------------------------------------------------------

CREATE TABLE IF NOT EXISTS public.premium_collection_items (
  id                  BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  premium_collection_id BIGINT NOT NULL REFERENCES public.premium_collections(id) ON DELETE CASCADE,
  order_index         INTEGER NOT NULL DEFAULT 0,
  item_type           TEXT NOT NULL
    CHECK (item_type IN ('gk_quiz', 'maths_quiz', 'passage_quiz', 'mains_question')),
  -- Exactly one of these will be populated
  quiz_id             BIGINT REFERENCES public.quizzes(id) ON DELETE CASCADE,
  passage_quiz_id     BIGINT REFERENCES public.passage_quizzes(id) ON DELETE CASCADE,
  mains_question_id   BIGINT REFERENCES public.mains_questions(id) ON DELETE CASCADE,
  -- Category assigned at item level (overrides question's own category for this test's analytics)
  category_id         BIGINT REFERENCES public.categories(id) ON DELETE SET NULL,
  created_at          TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  CHECK (
    (quiz_id IS NOT NULL)::INT +
    (passage_quiz_id IS NOT NULL)::INT +
    (mains_question_id IS NOT NULL)::INT = 1
  )
);

CREATE INDEX IF NOT EXISTS idx_prem_coll_items_collection ON public.premium_collection_items(premium_collection_id);
CREATE INDEX IF NOT EXISTS idx_prem_coll_items_order      ON public.premium_collection_items(premium_collection_id, order_index);

