-- Premium Collections: access-gated read, creator/admin write
DROP POLICY IF EXISTS "prem_coll_access_read" ON public.premium_collections;
CREATE POLICY "prem_coll_access_read" ON public.premium_collections
  FOR SELECT TO anon, authenticated USING (public.can_access_premium_collection(id));

DROP POLICY IF EXISTS "prem_coll_creator_or_admin_write" ON public.premium_collections;
CREATE POLICY "prem_coll_creator_or_admin_write" ON public.premium_collections FOR ALL TO authenticated
  USING (
    public.is_admin_or_moderator()
    OR EXISTS (SELECT 1 FROM public.profiles p WHERE p.id = premium_collections.creator_id AND p.auth_user_id = auth.uid())
  )
  WITH CHECK (
    public.is_admin_or_moderator()
    OR EXISTS (SELECT 1 FROM public.profiles p WHERE p.id = premium_collections.creator_id AND p.auth_user_id = auth.uid())
  );

