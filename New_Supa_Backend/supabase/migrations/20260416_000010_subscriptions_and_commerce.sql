-- ---------------------------------------------------------------
-- USER CONTENT ACCESS (Unified gating table)
-- Covers both series-based and direct collection purchases
-- ---------------------------------------------------------------

CREATE TABLE IF NOT EXISTS public.user_content_access (
  id              BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  user_id         BIGINT NOT NULL REFERENCES public.profiles(id) ON DELETE CASCADE,
  access_type     TEXT   NOT NULL CHECK (access_type IN ('test_series', 'collection', 'subscription')),
  test_series_id  BIGINT REFERENCES public.test_series(id) ON DELETE CASCADE,
  collection_id   BIGINT REFERENCES public.premium_collections(id) ON DELETE CASCADE,
  payment_id      BIGINT REFERENCES public.payments(id) ON DELETE SET NULL,
  granted_at      TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  expires_at      TIMESTAMPTZ,   -- NULL = never expires
  is_active       BOOLEAN NOT NULL DEFAULT TRUE,
  UNIQUE NULLS NOT DISTINCT (user_id, test_series_id, collection_id)
);

CREATE INDEX IF NOT EXISTS idx_uca_user         ON public.user_content_access(user_id);
CREATE INDEX IF NOT EXISTS idx_uca_series       ON public.user_content_access(test_series_id);
CREATE INDEX IF NOT EXISTS idx_uca_collection   ON public.user_content_access(collection_id);
CREATE INDEX IF NOT EXISTS idx_uca_expires      ON public.user_content_access(expires_at);

