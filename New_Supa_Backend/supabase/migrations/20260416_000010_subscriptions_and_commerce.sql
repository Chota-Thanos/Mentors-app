-- ---------------------------------------------------------------
-- SUBSCRIPTIONS (per-user active plan)
-- ---------------------------------------------------------------

CREATE TABLE IF NOT EXISTS public.subscriptions (
  id                       BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  user_id                  BIGINT NOT NULL REFERENCES public.profiles(id) ON DELETE CASCADE,
  plan_id                  BIGINT REFERENCES public.subscription_plans(id) ON DELETE SET NULL,
  plan                     TEXT NOT NULL DEFAULT 'free',
  start_date               TIMESTAMPTZ NOT NULL,
  end_date                 TIMESTAMPTZ NOT NULL,
  status                   TEXT NOT NULL DEFAULT 'active'
    CHECK (status IN ('active', 'pending', 'inactive', 'cancelled')),
  razorpay_subscription_id TEXT UNIQUE,
  last_razorpay_payment_id TEXT,
  created_at               TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  updated_at               TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

CREATE INDEX IF NOT EXISTS idx_subscriptions_user_status ON public.subscriptions(user_id, status);
CREATE INDEX IF NOT EXISTS idx_subscriptions_plan        ON public.subscriptions(plan, status);

DROP TRIGGER IF EXISTS trg_subscriptions_set_updated_at ON public.subscriptions;
CREATE TRIGGER trg_subscriptions_set_updated_at
  BEFORE UPDATE ON public.subscriptions
  FOR EACH ROW EXECUTE FUNCTION public.set_updated_at();

CREATE OR REPLACE FUNCTION public.has_active_subscription(required_plan TEXT DEFAULT 'pro')
RETURNS BOOLEAN LANGUAGE SQL STABLE AS $$
  SELECT EXISTS (
    SELECT 1 FROM public.subscriptions s
    JOIN public.profiles p ON p.id = s.user_id
    WHERE p.auth_user_id = auth.uid()
      AND s.status = 'active'
      AND s.end_date >= NOW()
      AND EXISTS (
        SELECT 1 FROM public.subscription_plans sp
        WHERE sp.id = s.plan_id
          AND sp.name = required_plan
          AND sp.can_access_subscribed_content = TRUE
      )
  );
$$;

-- ---------------------------------------------------------------
-- PAYMENTS (Full transaction record)
-- ---------------------------------------------------------------

CREATE TABLE IF NOT EXISTS public.payments (
  id                  BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  user_id             BIGINT NOT NULL REFERENCES public.profiles(id) ON DELETE RESTRICT,
  amount              NUMERIC(10,2) NOT NULL,
  currency            TEXT NOT NULL DEFAULT 'INR',
  status              TEXT NOT NULL DEFAULT 'created'
    CHECK (status IN ('created', 'paid', 'failed', 'refunded')),
  gateway             TEXT NOT NULL DEFAULT 'razorpay',
  gateway_order_id    TEXT UNIQUE,
  gateway_payment_id  TEXT,
  gateway_signature   TEXT,
  -- What was purchased (only one populated)
  test_series_id      BIGINT REFERENCES public.test_series(id) ON DELETE SET NULL,
  collection_id       BIGINT REFERENCES public.premium_collections(id) ON DELETE SET NULL,
  subscription_plan   TEXT,    -- plan name if purchasing a subscription
  -- Notes
  notes               TEXT,
  created_at          TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  updated_at          TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

CREATE INDEX IF NOT EXISTS idx_payments_user_id   ON public.payments(user_id);
CREATE INDEX IF NOT EXISTS idx_payments_status    ON public.payments(status);
CREATE INDEX IF NOT EXISTS idx_payments_gateway   ON public.payments(gateway_order_id);
CREATE INDEX IF NOT EXISTS idx_payments_series    ON public.payments(test_series_id);
CREATE INDEX IF NOT EXISTS idx_payments_collection ON public.payments(collection_id);

DROP TRIGGER IF EXISTS trg_payments_set_updated_at ON public.payments;
CREATE TRIGGER trg_payments_set_updated_at
  BEFORE UPDATE ON public.payments
  FOR EACH ROW EXECUTE FUNCTION public.set_updated_at();

