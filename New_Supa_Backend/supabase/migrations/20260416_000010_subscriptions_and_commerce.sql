CREATE OR REPLACE FUNCTION public.can_access_premium_collection(target_id BIGINT)
RETURNS BOOLEAN LANGUAGE SQL STABLE AS $$
  SELECT EXISTS (
    SELECT 1 FROM public.premium_collections pc
    LEFT JOIN public.profiles owner ON owner.id = pc.creator_id
    WHERE pc.id = target_id AND (
      public.is_admin_or_moderator()
      OR owner.auth_user_id = auth.uid()
      OR (pc.is_public = TRUE AND pc.is_paid = FALSE AND pc.is_subscription = FALSE)
      OR (pc.is_subscription = TRUE AND EXISTS (
          SELECT 1 FROM public.subscriptions s
          JOIN public.profiles up ON up.id = s.user_id
          WHERE up.auth_user_id = auth.uid()
            AND s.status = 'active'
            AND s.end_date >= NOW()
      ))
      OR EXISTS (
          SELECT 1 FROM public.user_content_access uca
          JOIN public.profiles up ON up.id = uca.user_id
          WHERE up.auth_user_id = auth.uid()
            AND uca.collection_id = target_id
            AND uca.is_active = TRUE
            AND (uca.expires_at IS NULL OR uca.expires_at > NOW())
      )
    )
  );
$$;

CREATE OR REPLACE FUNCTION public.can_access_test_series(target_id BIGINT)
RETURNS BOOLEAN LANGUAGE SQL STABLE AS $$
  SELECT EXISTS (
    SELECT 1 FROM public.test_series ts
    LEFT JOIN public.profiles owner ON owner.id = ts.creator_id
    WHERE ts.id = target_id AND (
      public.is_admin_or_moderator()
      OR owner.auth_user_id = auth.uid()
      OR (ts.is_public = TRUE AND ts.is_paid = FALSE AND ts.is_subscription = FALSE)
      OR (ts.is_subscription = TRUE AND EXISTS (
          SELECT 1 FROM public.subscriptions s
          JOIN public.profiles up ON up.id = s.user_id
          WHERE up.auth_user_id = auth.uid()
            AND s.status = 'active'
            AND s.end_date >= NOW()
      ))
      OR EXISTS (
          SELECT 1 FROM public.user_content_access uca
          JOIN public.profiles up ON up.id = uca.user_id
          WHERE up.auth_user_id = auth.uid()
            AND uca.test_series_id = target_id
            AND uca.is_active = TRUE
            AND (uca.expires_at IS NULL OR uca.expires_at > NOW())
      )
    )
  );
$$;
