-- Article Relations (embed / link between articles)
CREATE TABLE IF NOT EXISTS public.article_relations (
  id                BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  source_article_id BIGINT NOT NULL REFERENCES public.articles(id) ON DELETE CASCADE,
  target_article_id BIGINT NOT NULL REFERENCES public.articles(id) ON DELETE CASCADE,
