CREATE POLICY "article_comments_delete_own_or_admin" ON public.article_comments
  FOR DELETE TO authenticated
  USING (
    public.is_admin_or_moderator()
    OR EXISTS (SELECT 1 FROM public.profiles p WHERE p.id = article_comments.author_id AND p.auth_user_id = auth.uid())
  );
