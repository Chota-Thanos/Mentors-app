-- Public-read / admin-write for all content tables
DO $$
DECLARE
  tbls TEXT[] := ARRAY[
    'articles','article_categories','article_tags','article_sections',
    'article_relations','linkable_resources','article_resource_links',
    'blogs','blog_posts','study_pages','study_page_alpha_categories',
    'study_page_lists','study_page_list_tag_fields','study_page_list_tag_values',
    'study_page_list_items','study_page_list_item_tag_values',
    'timelines','timeline_events'
  ];
  t TEXT;
BEGIN
  FOREACH t IN ARRAY tbls LOOP
    EXECUTE FORMAT('DROP POLICY IF EXISTS "%s_public_read" ON public.%I', t, t);
    EXECUTE FORMAT(
      'CREATE POLICY "%s_public_read" ON public.%I FOR SELECT TO anon, authenticated USING (TRUE)', t, t
    );
    EXECUTE FORMAT('DROP POLICY IF EXISTS "%s_admin_write" ON public.%I', t, t);
    EXECUTE FORMAT(
      'CREATE POLICY "%s_admin_write" ON public.%I FOR ALL TO authenticated
       USING (public.is_admin_or_moderator()) WITH CHECK (public.is_admin_or_moderator())',
      t, t
    );
  END LOOP;
END;
$$;

-- Article likes — users manage their own
DROP POLICY IF EXISTS "article_likes_insert_own" ON public.article_likes;
CREATE POLICY "article_likes_insert_own" ON public.article_likes
  FOR INSERT TO authenticated
  WITH CHECK (EXISTS (SELECT 1 FROM public.profiles p WHERE p.id = article_likes.user_id AND p.auth_user_id = auth.uid()));

DROP POLICY IF EXISTS "article_likes_delete_own" ON public.article_likes;
CREATE POLICY "article_likes_delete_own" ON public.article_likes
  FOR DELETE TO authenticated
  USING (EXISTS (SELECT 1 FROM public.profiles p WHERE p.id = article_likes.user_id AND p.auth_user_id = auth.uid()));

