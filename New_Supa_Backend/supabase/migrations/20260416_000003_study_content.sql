CREATE TABLE IF NOT EXISTS public.articles (
  id                  BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  title               TEXT        NOT NULL,
  slug                TEXT        UNIQUE,
  summary             TEXT,
  excerpt             TEXT,
  main_article        TEXT,
  linked_summary      TEXT,
  linked_main_article TEXT,
  image_url           TEXT,
  featured_image      TEXT,
  -- Taxonomy
  subject_id          BIGINT REFERENCES public.subjects(id) ON DELETE SET NULL,
  topic_id            BIGINT REFERENCES public.topics(id) ON DELETE SET NULL,
  subtopic_id         BIGINT REFERENCES public.subtopics(id) ON DELETE SET NULL,
  source_id           BIGINT REFERENCES public.sources(id) ON DELETE SET NULL,
  source_url          TEXT,
  news_date           DATE,
  -- SEO
  meta_description    TEXT,
  meta_keywords       TEXT,
  seo_title           TEXT,
  seo_description     TEXT,
  -- State
  status              TEXT NOT NULL DEFAULT 'draft'
    CHECK (status IN ('draft', 'published', 'archived')),
  published_at        TIMESTAMPTZ,
  views_count         INTEGER NOT NULL DEFAULT 0,
  -- Authorship
  author_id           BIGINT NOT NULL REFERENCES public.profiles(id) ON DELETE RESTRICT,
  posted_at           TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  keywords            JSONB NOT NULL DEFAULT '[]'::JSONB,
  sources             JSONB NOT NULL DEFAULT '[]'::JSONB,
  created_at          TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  updated_at          TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

CREATE INDEX IF NOT EXISTS idx_articles_author_id     ON public.articles(author_id);
CREATE INDEX IF NOT EXISTS idx_articles_topic_id      ON public.articles(topic_id);
CREATE INDEX IF NOT EXISTS idx_articles_source_id     ON public.articles(source_id);
CREATE INDEX IF NOT EXISTS idx_articles_status_pub    ON public.articles(status, published_at DESC);
CREATE INDEX IF NOT EXISTS idx_articles_news_date     ON public.articles(news_date DESC);
CREATE INDEX IF NOT EXISTS idx_articles_posted_at     ON public.articles(posted_at DESC);

DROP TRIGGER IF EXISTS trg_articles_set_updated_at ON public.articles;
CREATE TRIGGER trg_articles_set_updated_at
  BEFORE UPDATE ON public.articles
  FOR EACH ROW EXECUTE FUNCTION public.set_updated_at();

-- Article → Category (using unified categories, domain='article')
CREATE TABLE IF NOT EXISTS public.article_categories (
