  FOR SELECT TO anon, authenticated USING (is_public = TRUE OR public.is_admin_or_moderator());
DROP POLICY IF EXISTS "creator_profiles_own_write" ON public.creator_profiles;
CREATE POLICY "creator_profiles_own_write" ON public.creator_profiles FOR ALL TO authenticated
  USING (
    public.is_admin_or_moderator()
    OR EXISTS (SELECT 1 FROM public.profiles p WHERE p.id = creator_profiles.user_id AND p.auth_user_id = auth.uid())
  )
  WITH CHECK (EXISTS (SELECT 1 FROM public.profiles p WHERE p.id = creator_profiles.user_id AND p.auth_user_id = auth.uid()));

