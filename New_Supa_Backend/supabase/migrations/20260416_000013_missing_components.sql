-- ---------------------------------------------------------------
-- CREATOR / MENTOR PROFILES
-- Extended public profile for users with creator roles.
-- Separate from auth profiles — stores professional details.
-- ---------------------------------------------------------------

CREATE TABLE IF NOT EXISTS public.creator_profiles (
  id                  BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  user_id             BIGINT NOT NULL UNIQUE REFERENCES public.profiles(id) ON DELETE CASCADE,
  display_name        TEXT   NOT NULL,
  headline            TEXT,
  bio                 TEXT,
  years_experience    INTEGER,
  city                TEXT,
  profile_image_url   TEXT,
  is_verified         BOOLEAN NOT NULL DEFAULT FALSE,
  is_public           BOOLEAN NOT NULL DEFAULT TRUE,
  is_active           BOOLEAN NOT NULL DEFAULT TRUE,
  -- Professional credentials / highlights
  highlights          JSONB NOT NULL DEFAULT '[]'::JSONB,
  -- e.g. [{"label": "IAS 2021 Topper", "icon": "trophy"}]
  credentials         JSONB NOT NULL DEFAULT '[]'::JSONB,
  specialization_tags JSONB NOT NULL DEFAULT '[]'::JSONB,
  languages           JSONB NOT NULL DEFAULT '[]'::JSONB,
  social_links        JSONB NOT NULL DEFAULT '{}'::JSONB,
  contact_url         TEXT,
  public_email        TEXT,
  created_at          TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  updated_at          TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

CREATE INDEX IF NOT EXISTS idx_creator_profiles_user     ON public.creator_profiles(user_id);
CREATE INDEX IF NOT EXISTS idx_creator_profiles_public   ON public.creator_profiles(is_public, is_active);
CREATE INDEX IF NOT EXISTS idx_creator_profiles_verified ON public.creator_profiles(is_verified);

DROP TRIGGER IF EXISTS trg_creator_profiles_set_updated_at ON public.creator_profiles;
CREATE TRIGGER trg_creator_profiles_set_updated_at
  BEFORE UPDATE ON public.creator_profiles
  FOR EACH ROW EXECUTE FUNCTION public.set_updated_at();

