-- ---------------------------------------------------------------
-- SUBJECTS → TOPICS → SUBTOPICS
-- Three-level hierarchy used by the study material system
-- ---------------------------------------------------------------

CREATE TABLE IF NOT EXISTS public.subjects (
  id            BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  name          TEXT        NOT NULL,
  slug          TEXT        NOT NULL UNIQUE,
  description   TEXT,
  display_order INTEGER,
  is_active     BOOLEAN NOT NULL DEFAULT TRUE,
  created_at    TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  updated_at    TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

DROP TRIGGER IF EXISTS trg_subjects_set_updated_at ON public.subjects;
CREATE TRIGGER trg_subjects_set_updated_at
  BEFORE UPDATE ON public.subjects
  FOR EACH ROW EXECUTE FUNCTION public.set_updated_at();

CREATE TABLE IF NOT EXISTS public.topics (
  id            BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  subject_id    BIGINT      NOT NULL REFERENCES public.subjects(id) ON DELETE CASCADE,
  name          TEXT        NOT NULL,
  slug          TEXT        NOT NULL,
  description   TEXT,
  has_prelims   BOOLEAN NOT NULL DEFAULT FALSE,
  has_mains     BOOLEAN NOT NULL DEFAULT FALSE,
  display_order INTEGER,
  is_active     BOOLEAN NOT NULL DEFAULT TRUE,
  created_at    TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  updated_at    TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  UNIQUE (subject_id, slug)
);

CREATE INDEX IF NOT EXISTS idx_topics_subject_id ON public.topics(subject_id);

DROP TRIGGER IF EXISTS trg_topics_set_updated_at ON public.topics;
CREATE TRIGGER trg_topics_set_updated_at
