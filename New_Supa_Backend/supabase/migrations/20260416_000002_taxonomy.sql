-- =============================================================
-- MIGRATION 2: Taxonomy — Exams, Categories, Sources, Subjects
-- =============================================================

-- ---------------------------------------------------------------
-- EXAMS (top-level filter for Test Series and Mentors only)
-- ---------------------------------------------------------------

CREATE TABLE IF NOT EXISTS public.exams (
  id          BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  name        TEXT NOT NULL UNIQUE,
  slug        TEXT NOT NULL UNIQUE,
  exam_type   TEXT NOT NULL DEFAULT 'combined'
    CHECK (exam_type IN ('combined', 'prelims_only', 'mains_only', 'other')),
  description TEXT,
  logo_url    TEXT,
  is_active   BOOLEAN     NOT NULL DEFAULT TRUE,
  created_at  TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  updated_at  TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

DROP TRIGGER IF EXISTS trg_exams_set_updated_at ON public.exams;
CREATE TRIGGER trg_exams_set_updated_at
  BEFORE UPDATE ON public.exams
  FOR EACH ROW EXECUTE FUNCTION public.set_updated_at();

-- ---------------------------------------------------------------
-- UNIFIED CATEGORIES
-- domain = which content system this tree belongs to
-- No exam references here — exams filter at the series level only
-- ---------------------------------------------------------------

CREATE TABLE IF NOT EXISTS public.categories (
  id            BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  name          TEXT NOT NULL,
  slug          TEXT UNIQUE,
  parent_id     BIGINT REFERENCES public.categories(id) ON DELETE SET NULL,
  domain        TEXT NOT NULL
    CHECK (domain IN ('gk', 'maths', 'passage', 'mains', 'article')),
  description   TEXT,
  is_active     BOOLEAN     NOT NULL DEFAULT TRUE,
  display_order INTEGER     NOT NULL DEFAULT 0,
  -- Denormalized counters (updated by triggers or background jobs)
  question_count        INTEGER NOT NULL DEFAULT 0,
  total_question_count  INTEGER NOT NULL DEFAULT 0,
  created_at    TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  updated_at    TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

CREATE INDEX IF NOT EXISTS idx_categories_parent_id   ON public.categories(parent_id);
CREATE INDEX IF NOT EXISTS idx_categories_domain       ON public.categories(domain);
CREATE INDEX IF NOT EXISTS idx_categories_parent_domain ON public.categories(parent_id, domain);

DROP TRIGGER IF EXISTS trg_categories_set_updated_at ON public.categories;
CREATE TRIGGER trg_categories_set_updated_at
  BEFORE UPDATE ON public.categories
  FOR EACH ROW EXECUTE FUNCTION public.set_updated_at();

