-- Uploaded PDFs
DROP POLICY IF EXISTS "uploaded_pdfs_own" ON public.uploaded_pdfs;
CREATE POLICY "uploaded_pdfs_own" ON public.uploaded_pdfs FOR ALL TO authenticated
  USING (
    public.is_admin_or_moderator()
    OR EXISTS (SELECT 1 FROM public.profiles p WHERE p.id = uploaded_pdfs.user_id AND p.auth_user_id = auth.uid())
  )
  WITH CHECK (EXISTS (SELECT 1 FROM public.profiles p WHERE p.id = uploaded_pdfs.user_id AND p.auth_user_id = auth.uid()));

-- AI Tests — owner or public read
DROP POLICY IF EXISTS "ai_tests_public_read" ON public.ai_tests;
CREATE POLICY "ai_tests_public_read" ON public.ai_tests
  FOR SELECT TO anon, authenticated USING (is_public = TRUE);

DROP POLICY IF EXISTS "ai_tests_own" ON public.ai_tests;
CREATE POLICY "ai_tests_own" ON public.ai_tests FOR ALL TO authenticated
  USING (EXISTS (SELECT 1 FROM public.profiles p WHERE p.id = ai_tests.user_id AND p.auth_user_id = auth.uid()))
  WITH CHECK (EXISTS (SELECT 1 FROM public.profiles p WHERE p.id = ai_tests.user_id AND p.auth_user_id = auth.uid()));

-- AI Test Questions — public if test is public, else owner only
DROP POLICY IF EXISTS "ai_test_questions_read" ON public.ai_test_questions;
CREATE POLICY "ai_test_questions_read" ON public.ai_test_questions
  FOR SELECT TO anon, authenticated
  USING (EXISTS (SELECT 1 FROM public.ai_tests t WHERE t.id = ai_test_questions.ai_test_id AND (t.is_public = TRUE OR EXISTS (SELECT 1 FROM public.profiles p WHERE p.id = t.user_id AND p.auth_user_id = auth.uid()))));

DROP POLICY IF EXISTS "ai_test_questions_own_write" ON public.ai_test_questions;
CREATE POLICY "ai_test_questions_own_write" ON public.ai_test_questions
  FOR ALL TO authenticated
  USING (EXISTS (SELECT 1 FROM public.ai_tests t JOIN public.profiles p ON p.id = t.user_id WHERE t.id = ai_test_questions.ai_test_id AND p.auth_user_id = auth.uid()))
  WITH CHECK (EXISTS (SELECT 1 FROM public.ai_tests t JOIN public.profiles p ON p.id = t.user_id WHERE t.id = ai_test_questions.ai_test_id AND p.auth_user_id = auth.uid()));

-- AI Test Attempts — own
DROP POLICY IF EXISTS "ai_test_attempts_own" ON public.ai_test_attempts;
CREATE POLICY "ai_test_attempts_own" ON public.ai_test_attempts FOR ALL TO authenticated
  USING (user_id IS NULL OR EXISTS (SELECT 1 FROM public.profiles p WHERE p.id = ai_test_attempts.user_id AND p.auth_user_id = auth.uid()))
  WITH CHECK (user_id IS NULL OR EXISTS (SELECT 1 FROM public.profiles p WHERE p.id = ai_test_attempts.user_id AND p.auth_user_id = auth.uid()));

-- AI Test Answers — own
DROP POLICY IF EXISTS "ai_test_answers_own" ON public.ai_test_answers;
