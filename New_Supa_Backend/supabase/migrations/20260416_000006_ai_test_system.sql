-- ---------------------------------------------------------------
-- UPLOADED PDFs (used as source for AI generation)
-- ---------------------------------------------------------------

CREATE TABLE IF NOT EXISTS public.uploaded_pdfs (
  id                  BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  user_id             BIGINT REFERENCES public.profiles(id) ON DELETE SET NULL,
  filename            TEXT   NOT NULL,
  content_type        TEXT,
  storage_object_path TEXT,
  extracted_text      TEXT,
  page_count          INTEGER,
  used_ocr            BOOLEAN NOT NULL DEFAULT FALSE,
  -- Lifecycle management
  status              TEXT NOT NULL DEFAULT 'active'
    CHECK (status IN ('active', 'expired', 'deleted')),
  expires_at          TIMESTAMPTZ,
  deleted_at          TIMESTAMPTZ,
  created_at          TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

CREATE INDEX IF NOT EXISTS idx_uploaded_pdfs_user    ON public.uploaded_pdfs(user_id);
CREATE INDEX IF NOT EXISTS idx_uploaded_pdfs_status  ON public.uploaded_pdfs(status);

-- ---------------------------------------------------------------
-- AI TESTS (Separate identity from creator programs)
-- Generated by any user; optionally made public
-- ---------------------------------------------------------------

CREATE TABLE IF NOT EXISTS public.ai_tests (
  id            BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  user_id       BIGINT NOT NULL REFERENCES public.profiles(id) ON DELETE CASCADE,
  title         TEXT   NOT NULL,
  quiz_domain   TEXT   NOT NULL CHECK (quiz_domain IN ('gk', 'maths', 'passage', 'mains')),
  format_id     BIGINT REFERENCES public.question_formats(id) ON DELETE SET NULL,
  -- Source of content for generation
  source_type   TEXT   CHECK (source_type IN ('text', 'url', 'pdf', 'category')),
  source_text   TEXT,
  source_url    TEXT,
  source_pdf_id BIGINT REFERENCES public.uploaded_pdfs(id) ON DELETE SET NULL,
  -- Generation parameters
  language      TEXT   NOT NULL DEFAULT 'en' CHECK (language IN ('en', 'hi')),
  question_count INTEGER,
  ai_instruction_id BIGINT REFERENCES public.ai_instructions(id) ON DELETE SET NULL,
  -- Visibility
  is_public     BOOLEAN NOT NULL DEFAULT FALSE,
  likes_count   INTEGER NOT NULL DEFAULT 0,
  created_at    TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  updated_at    TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

CREATE INDEX IF NOT EXISTS idx_ai_tests_user_id    ON public.ai_tests(user_id);
CREATE INDEX IF NOT EXISTS idx_ai_tests_domain     ON public.ai_tests(quiz_domain);
CREATE INDEX IF NOT EXISTS idx_ai_tests_public     ON public.ai_tests(is_public, created_at DESC);

DROP TRIGGER IF EXISTS trg_ai_tests_set_updated_at ON public.ai_tests;
CREATE TRIGGER trg_ai_tests_set_updated_at
  BEFORE UPDATE ON public.ai_tests
  FOR EACH ROW EXECUTE FUNCTION public.set_updated_at();

-- Category filter for AI test (when source_type = 'category')
CREATE TABLE IF NOT EXISTS public.ai_test_categories (
  ai_test_id  BIGINT NOT NULL REFERENCES public.ai_tests(id) ON DELETE CASCADE,
  category_id BIGINT NOT NULL REFERENCES public.categories(id) ON DELETE CASCADE,
  PRIMARY KEY (ai_test_id, category_id)
);

-- ---------------------------------------------------------------
-- AI TEST QUESTIONS (Individual questions within an AI test)
-- ---------------------------------------------------------------

CREATE TABLE IF NOT EXISTS public.ai_test_questions (
  id            BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  ai_test_id    BIGINT NOT NULL REFERENCES public.ai_tests(id) ON DELETE CASCADE,
  quiz_domain   TEXT   NOT NULL CHECK (quiz_domain IN ('gk', 'maths', 'passage', 'mains')),
  -- Normalized question structure (same fields as quiz_content, stored as JSON here
  -- because AI test questions may not yet be "promoted" to official quiz pool)
  question_statement      TEXT,
  supp_question_statement TEXT,
  statements_facts        JSONB NOT NULL DEFAULT '[]'::JSONB,
  question_prompt         TEXT,
  options                 JSONB NOT NULL DEFAULT '[]'::JSONB,
  correct_answer          TEXT,
  explanation             TEXT,
  passage_title           TEXT,      -- for passage-type questions
  passage_text            TEXT,
  -- Category (for analytics tracking)
  category_id             BIGINT REFERENCES public.categories(id) ON DELETE SET NULL,
  display_order           INTEGER NOT NULL DEFAULT 0,
  is_edited               BOOLEAN NOT NULL DEFAULT FALSE,
  created_at              TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  updated_at              TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

CREATE INDEX IF NOT EXISTS idx_ai_test_questions_test     ON public.ai_test_questions(ai_test_id);
CREATE INDEX IF NOT EXISTS idx_ai_test_questions_category ON public.ai_test_questions(category_id);

DROP TRIGGER IF EXISTS trg_ai_test_questions_set_updated_at ON public.ai_test_questions;
CREATE TRIGGER trg_ai_test_questions_set_updated_at
  BEFORE UPDATE ON public.ai_test_questions
  FOR EACH ROW EXECUTE FUNCTION public.set_updated_at();

-- ---------------------------------------------------------------
-- AI TEST ATTEMPTS & ANSWERS (Track user performance on AI tests)
-- ---------------------------------------------------------------

CREATE TABLE IF NOT EXISTS public.ai_test_attempts (
  id           BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  ai_test_id   BIGINT NOT NULL REFERENCES public.ai_tests(id) ON DELETE CASCADE,
  user_id      BIGINT REFERENCES public.profiles(id) ON DELETE SET NULL,
  submitted_at TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

CREATE INDEX IF NOT EXISTS idx_ai_test_attempts_test    ON public.ai_test_attempts(ai_test_id);
CREATE INDEX IF NOT EXISTS idx_ai_test_attempts_user    ON public.ai_test_attempts(user_id);

CREATE TABLE IF NOT EXISTS public.ai_test_answers (
  id                   BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  attempt_id           BIGINT NOT NULL REFERENCES public.ai_test_attempts(id) ON DELETE CASCADE,
  ai_test_question_id  BIGINT NOT NULL REFERENCES public.ai_test_questions(id) ON DELETE CASCADE,
  selected_option      TEXT,
  is_correct           BOOLEAN,
  is_skipped           BOOLEAN NOT NULL DEFAULT FALSE,
  time_spent_secs      INTEGER,
  category_id          BIGINT REFERENCES public.categories(id) ON DELETE SET NULL
);

CREATE INDEX IF NOT EXISTS idx_ai_test_answers_attempt  ON public.ai_test_answers(attempt_id);
CREATE INDEX IF NOT EXISTS idx_ai_test_answers_question ON public.ai_test_answers(ai_test_question_id);

