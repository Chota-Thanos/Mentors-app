ALTER TABLE public.mains_test_copy_submissions       ENABLE ROW LEVEL SECURITY;
ALTER TABLE public.mains_program_mentorship_requests ENABLE ROW LEVEL SECURITY;

-- Submissions: own user + series creator + admin
DROP POLICY IF EXISTS "mains_subs_read" ON public.mains_test_copy_submissions;
CREATE POLICY "mains_subs_read" ON public.mains_test_copy_submissions
  FOR SELECT TO authenticated
  USING (
    public.is_admin_or_moderator()
    OR EXISTS (SELECT 1 FROM public.profiles p WHERE p.id = mains_test_copy_submissions.user_id AND p.auth_user_id = auth.uid())
    OR EXISTS (SELECT 1 FROM public.test_series ts JOIN public.profiles p ON p.id = ts.creator_id WHERE ts.id = mains_test_copy_submissions.series_id AND p.auth_user_id = auth.uid())
  );

DROP POLICY IF EXISTS "mains_subs_user_insert" ON public.mains_test_copy_submissions;
CREATE POLICY "mains_subs_user_insert" ON public.mains_test_copy_submissions
  FOR INSERT TO authenticated
  WITH CHECK (EXISTS (SELECT 1 FROM public.profiles p WHERE p.id = mains_test_copy_submissions.user_id AND p.auth_user_id = auth.uid()));

DROP POLICY IF EXISTS "mains_subs_admin_update" ON public.mains_test_copy_submissions;
CREATE POLICY "mains_subs_admin_update" ON public.mains_test_copy_submissions
  FOR UPDATE TO authenticated
  USING (
    public.is_admin_or_moderator()
    OR EXISTS (SELECT 1 FROM public.test_series ts JOIN public.profiles p ON p.id = ts.creator_id WHERE ts.id = mains_test_copy_submissions.series_id AND p.auth_user_id = auth.uid())
  )
  WITH CHECK (
    public.is_admin_or_moderator()
    OR EXISTS (SELECT 1 FROM public.test_series ts JOIN public.profiles p ON p.id = ts.creator_id WHERE ts.id = mains_test_copy_submissions.series_id AND p.auth_user_id = auth.uid())
  );

